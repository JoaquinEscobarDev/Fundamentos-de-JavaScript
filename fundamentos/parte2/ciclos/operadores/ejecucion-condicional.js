/*
Condicional IF
El condicional if se utiliza para ejecutar un bloque de código si una condición es verdadera.
*/

let nombre = "Diego";

if (nombre === "Diego") {
    console.log("Hola Diego");
}else if (nombre === "Juan") {
    console.log("Hola Juan");
}else{
    console.log("No eres Diego o Juan");

}
